#include "libft.h"

long	ft_atol(const char *nptr)
{
	int		i;
	int		signo;
	long	resultado;

	i = 0;
	signo = 1;
	resultado = 0;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || nptr[i] == 32)
		i++;
	if (nptr[i] == '-')
	{
		signo = -1;
		i++;
	}
	else if (nptr[i] == '+')
		i++;
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		resultado = resultado * 10 + (nptr[i] - '0');
		i++;
	}
	return (signo * resultado);
}
